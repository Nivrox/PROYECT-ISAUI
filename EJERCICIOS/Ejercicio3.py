#scribe un programa en Python que solicite al usuario que ingrese la base y la altura de un triángulo
#y luego calcule y muestre el área del triángulo.
base = float (input ("ingrese la base del triangulo "))
altura = float (input ("ingrese la altura del triangulo "))
area = (base * altura) / 2 
print ("El area de su triangulo es de", area , "m")
